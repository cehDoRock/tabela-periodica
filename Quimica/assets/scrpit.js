var type = document.querySelector("#element");

function queCorEuColoco(e){
    // console.log(e.tipo)
    // type.setAttribute("class","")
    if(e.tipo === "Não metal"){
        type.setAttribute("class", "naoMetal")
    }else if(e.tipo ===  "Metal alcalino terroso"){
        type.setAttribute("class", "metaisAlcalinos-terrosos")
    }else if(e.tipo ===  "Outros metais"){
        type.setAttribute("class", "outrosMetais")
    }else if(e.tipo ===  "Actinídeo"){
        type.setAttribute("class", "Actinidios")
    }else if(e.tipo === "Gás nobre"){
        type.setAttribute("class", "gasNobre")
    }else if(e.tipo ==="Semimetal"){
        type.setAttribute("class", "semimetais")
    }else if(e.tipo ==="Metal de transição"){
        type.setAttribute("class", "metaisDeTransicao")
    }else if(e.tipo ==="Metal alcalino"){
        type.setAttribute("class", "metaisAlcalinos")
    }else if(e.tipo ==="Halogênio"){
        type.setAttribute("class", "haloenios")
    }else if(e.tipo ==="Lantanídeo"){
        type.setAttribute("class", "lantanideos")
    }else if(e.tipo ==="nan"){
        type.setAttribute("class", "nan")
    }
}
